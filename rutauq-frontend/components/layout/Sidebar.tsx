interface SidebarProps {
  className?: string;
}

function NavLink({ item, pathname }: { item: NavItem; pathname: string }) {
  const active = item.exact
    ? pathname === item.href
    : pathname === item.href || pathname.startsWith(item.href + "/");
  return (
    <Link
      href={item.href}
      className={cn(
        "flex items-center gap-3 rounded-md px-3 py-2 text-sm font-medium transition-colors",
        active
          ? "bg-primary-50 text-primary-700"
          : "text-neutral-600 hover:bg-neutral-100 hover:text-neutral-900"
      )}
    >
      <span className={cn(active ? "text-primary-600" : "text-neutral-400")}>
        {item.icon}
      </span>
      {item.label}
    </Link>
  );
}

export default function Sidebar({ className }: SidebarProps) {
  const pathname = usePathname();
  const { user } = useAuthStore();

  const visibleItems = navItems.filter(
    (item) => !item.roles || (user && item.roles.includes(user.role))
  );

  const isAdmin = user?.role === "ADMIN";

  return (
    <aside
      className={cn(
        "flex w-56 flex-shrink-0 flex-col border-r border-neutral-200 bg-white",
        className
      )}
    >
      <nav className="flex flex-col gap-1 p-3">
        {visibleItems.map((item) => (
          <NavLink key={item.href} item={item} pathname={pathname} />
        ))}

        {isAdmin && (
          <>
            <div className="my-2 border-t border-neutral-100" />
            <p className="px-3 pb-1 text-xs font-semibold uppercase tracking-wide text-neutral-400">
              Admin
            </p>
            {adminItems.map((item) => (
              <NavLink key={item.href} item={item} pathname={pathname} />
            ))}
          </>
        )}
      </nav>
    </aside>
  );
}
